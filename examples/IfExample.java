// IfExample.java
// Shows and example of if statements in java

public class IfExample {
	public static void main(String[] args) {
		int a=100, b=50;
		if(a > b) {
			System.out.println("a is greater than b");
		}
		if(true) {
			System.out.println("This was printed because the evaluation is true");
		}
		if(false) {
			System.out.println("This was printed because the evaluation is false");
		}

	}
}
